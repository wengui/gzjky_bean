package com.gzjky.bean.gen;

public class NowComplicationsConfig {
    private Integer id;

    private String diseasename;

    private String diseasecontent;

    private String diseasecontentprompt;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDiseasename() {
        return diseasename;
    }

    public void setDiseasename(String diseasename) {
        this.diseasename = diseasename;
    }

    public String getDiseasecontent() {
        return diseasecontent;
    }

    public void setDiseasecontent(String diseasecontent) {
        this.diseasecontent = diseasecontent;
    }

    public String getDiseasecontentprompt() {
        return diseasecontentprompt;
    }

    public void setDiseasecontentprompt(String diseasecontentprompt) {
        this.diseasecontentprompt = diseasecontentprompt;
    }
}